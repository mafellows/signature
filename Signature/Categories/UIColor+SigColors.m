#import "UIColor+SigColors.h"

@implementation UIColor (SigColors)

+ (UIColor *)sigLightGray
{
    return [UIColor colorWithRed:247.0f / 255.0f green:247.0f / 255.0f blue:247.0f / 255.0f alpha:1.0f];
}

+ (UIColor *)sigGray
{
    return [UIColor colorWithRed:199.0f / 255.0f green:199.0f / 255.0f blue:204.0f / 255.0f alpha:1.0f]; 
}

@end
